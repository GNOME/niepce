#pragma once

#include <algorithm>
#include <map>

namespace fwk {

  /** call operator delete on all the data element. */
  template <typename _Map>
  void map_delete_all_second(const _Map & m)
  {
    std::for_each(m.cbegin(), m.cend(),
                  [] (const typename _Map::value_type & p) {
                      delete p.second;
                  }
        );
  }

}

